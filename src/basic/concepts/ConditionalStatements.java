package basic.concepts;

public class ConditionalStatements {
	//we can use to execute required block of statements based on give TRUE/FALSE 
	//1)if condition
	//2)else if condition
	//3)nested if
	//4)switch case

	public static void main(String[] args) {
		//if condition =it allow to execute particular block of statements when expression TRUE
		int a=10;
		int b=20;
		
		if (a<b) {
			System.out.println("having smaller number value:"+a);
			
		}
		//else if =using else if we can execute diff block of statements based on given condition TRUE/FALSE
		else {
			System.out.println("having smaller number value:"+b);
		}
		}
		}
		
	


